package com.code.model;

public class StatLine {
	private float points;
	private float rebounds;
	private float assists;
	private float blocks;
	private float steals;
	private float twoPointFG;
	private float threePointFG;
	private float freeThrows;
	private float turnovers;
	
	public float getPoints() {
		return points;
	}
	public void setPoints(float points) {
		this.points = points;
	}
	public float getRebounds() {
		return rebounds;
	}
	public void setRebounds(float rebounds) {
		this.rebounds = rebounds;
	}
	public float getAssists() {
		return assists;
	}
	public void setAssists(float assists) {
		this.assists = assists;
	}
	public float getBlocks() {
		return blocks;
	}
	public void setBlocks(float blocks) {
		this.blocks = blocks;
	}
	public float getSteals() {
		return steals;
	}
	public void setSteals(float steals) {
		this.steals = steals;
	}
	public float getTwoPointFG() {
		return twoPointFG;
	}
	public void setTwoPointFG(float twoPointFG) {
		this.twoPointFG = twoPointFG;
	}
	public float getThreePointFG() {
		return threePointFG;
	}
	public void setThreePointFG(float threePointFG) {
		this.threePointFG = threePointFG;
	}
	public float getFreeThrows() {
		return freeThrows;
	}
	public void setFreeThrows(float freeThrows) {
		this.freeThrows = freeThrows;
	}
	public float getTurnovers() {
		return turnovers;
	}
	public void setTurnovers(float turnovers) {
		this.turnovers = turnovers;
	}
}
